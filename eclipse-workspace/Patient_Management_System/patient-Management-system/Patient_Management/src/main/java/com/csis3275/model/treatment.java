package com.csis3275.model;

public class treatment {
	private String patientFirstName;
	private String patientLastName;
	private String patientMedicine;
	private String patientAlleries;
	private int medicinedoses;
	private String specialNote;
	
	

	public String getPatientFirstName() {
		return patientFirstName;
	}
	public void setPatientFirstName(String patientFirstName) {
		this.patientFirstName = patientFirstName;
	}
	public String getPatientLastName() {
		return patientLastName;
	}
	public void setPatientLastName(String patientLastName) {
		this.patientLastName = patientLastName;
	}
	public String getPatientMedicine() {
		return patientMedicine;
	}
	public void setPatientMedicine(String patientMedicine) {
		this.patientMedicine = patientMedicine;
	}
	public String getPatientAlleries() {
		return patientAlleries;
	}
	public void setPatientAlleries(String patientAlleries) {
		this.patientAlleries = patientAlleries;
	}
	public int getMedicinedoses() {
		return medicinedoses;
	}
	public void setMedicinedoses(int medicinedoses) {
		this.medicinedoses = medicinedoses;
	}
	public String getSpecialNote() {
		return specialNote;
	}
	public void setSpecialNote(String specialNote) {
		this.specialNote = specialNote;
	}
	

}
